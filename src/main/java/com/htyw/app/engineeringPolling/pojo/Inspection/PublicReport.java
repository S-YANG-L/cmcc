package com.htyw.app.engineeringPolling.pojo.Inspection;

import java.util.Date;

//公共报事
public class PublicReport {
    private int id;
    private String reportType;
    private String publicReportType;
    private String inspectionPerson;
    private String publicReportLocation;
    private Date reportTime;
    private String reportContent;
    private String reportPictureFile;
    private int tenantId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getReportType() {
        return reportType;
    }

    public void setReportType(String reportType) {
        this.reportType = reportType;
    }

    public String getPublicReportType() {
        return publicReportType;
    }

    public void setPublicReportType(String publicReportType) {
        this.publicReportType = publicReportType;
    }

    public String getInspectionPerson() {
        return inspectionPerson;
    }

    public void setInspectionPerson(String inspectionPerson) {
        this.inspectionPerson = inspectionPerson;
    }

    public String getPublicReportLocation() {
        return publicReportLocation;
    }

    public void setPublicReportLocation(String publicReportLocation) {
        this.publicReportLocation = publicReportLocation;
    }

    public Date getReportTime() {
        return reportTime;
    }

    public void setReportTime(Date reportTime) {
        this.reportTime = reportTime;
    }

    public String getReportContent() {
        return reportContent;
    }

    public void setReportContent(String reportContent) {
        this.reportContent = reportContent;
    }

    public String getReportPictureFile() {
        return reportPictureFile;
    }

    public void setReportPictureFile(String reportPictureFile) {
        this.reportPictureFile = reportPictureFile;
    }

    public int getTenantId() {
        return tenantId;
    }

    public void setTenantId(int tenantId) {
        this.tenantId = tenantId;
    }
}
