package com.htyw.app.serviceCenter.pojo;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

public class OrderBusinessDraft {
	
private Long id;  //主键
private Long realEstateCcustomerId;  //房产客户ID
private String realEstateCcustomer;  //房产客户
private String houseName;  //房间名称
private String contacts;  //联系人
private String contactNumber;  //联系电话
private String contactAddress;  //联系地址
private Date appointmentDate;  //预约日期
private String orderContent;  //单据内容
private String orderType;  //单据类型
private Long tenantId;  //租户ID
private String remark;  //备注

public Long getId() {
	return id;
}
public void setId(Long id) {
	this.id = id;
}
public Long getRealEstateCcustomerId() {
	return realEstateCcustomerId;
}
public void setRealEstateCcustomerId(Long realEstateCcustomerId) {
	this.realEstateCcustomerId = realEstateCcustomerId;
}
public String getRealEstateCcustomer() {
	return realEstateCcustomer;
}
public void setRealEstateCcustomer(String realEstateCcustomer) {
	this.realEstateCcustomer = realEstateCcustomer;
}
public String getHouseName() {
	return houseName;
}
public void setHouseName(String houseName) {
	this.houseName = houseName;
}
public String getContacts() {
	return contacts;
}
public void setContacts(String contacts) {
	this.contacts = contacts;
}
public String getContactNumber() {
	return contactNumber;
}
public void setContactNumber(String contactNumber) {
	this.contactNumber = contactNumber;
}
public String getContactAddress() {
	return contactAddress;
}
public void setContactAddress(String contactAddress) {
	this.contactAddress = contactAddress;
}
@JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss")
public Date getAppointmentDate() {
	return appointmentDate;
}
public void setAppointmentDate(Date appointmentDate) {
	this.appointmentDate = appointmentDate;
}
public String getOrderContent() {
	return orderContent;
}
public void setOrderContent(String orderContent) {
	this.orderContent = orderContent;
}
public String getOrderType() {
	return orderType;
}
public void setOrderType(String orderType) {
	this.orderType = orderType;
}
public Long getTenantId() {
	return tenantId;
}
public void setTenantId(Long tenantId) {
	this.tenantId = tenantId;
}
public String getRemark() {
	return remark;
}
public void setRemark(String remark) {
	this.remark = remark;
}
}
